package aula2;

public class Calculadora {
    
    double numA;
    double numB;

    double somar(){
        return numA+numB;
    }
    double subtrair(){
        return numA-numB;
    }
    double multiplicar(){
        return numA*numB;
    }
    double dividir(){
        return numA/numB;
    }

}
